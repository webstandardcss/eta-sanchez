    <h4>Secretary Napolitano Announces Deferred Action Process for Young People Who Are Low Enforcement Priorities</h4>
	<p><strong>Release Date: June 15, 2012</strong></p>
    
    <p>
      For Immediate Release<br />
      Office of the Press Secretary<br />
      Contact: 202-282-8010
    </p>
<p>La Secretaria Napolitano Anuncia Proceso De Accion Diferida Para Jovenes Que Sean De Baja Prioridad Para La Aplicacion De La Ley</p>
<p>WASHINGTON - Secretary of Homeland Security Janet Napolitano today announced that effective immediately, certain young people who were brought to the United States as young children, do not present a risk to national security or public safety, and meet
  several key criteria will be considered for relief from removal from the country or from entering into removal proceedings. Those who demonstrate that they meet the criteria will be eligible to receive deferred action for a period of two years, subject
  to renewal, and will be eligible to apply for work authorization.</p>
<p>"Our nation's immigration laws must be enforced in a firm and sensible manner," said Secretary Napolitano. "But they are not designed to be blindly enforced without consideration given to the individual circumstances of each case. Nor are they designed
  to remove productive young people to countries where they may not have lived or even speak the language. Discretion, which is used in so many other areas, is especially justified here."</p>
<p>DHS continues to focus its enforcement resources on the removal of individuals who pose a national security or public safety risk, including immigrants convicted of crimes, violent criminals, felons, and repeat immigration law offenders. Today's action
  further enhances the Department's ability to focus on these priority removals.</p>
<p>Under this directive, individuals who demonstrate that they meet the following criteria will be eligible for an exercise of discretion, specifically deferred action, on a case by case basis:Came to the United States under the age of sixteen;Have continuously
  resided in the United States for a least five years preceding the date of this memorandum and are present in the United States on the date of this memorandum;Are currently in school, have graduated from high school, have obtained a general education
  development certificate, or are honorably discharged veterans of the Coast Guard or Armed Forces of the United States;Have not been convicted of a felony offense, a significant misdemeanor offense, multiple misdemeanor offenses, or otherwise pose a
  threat to national security or public safety;Are not above the age of thirty.</p>
<p>Only those individuals who can prove through verifiable documentation that they meet these criteria will be eligible for deferred action. Individuals will not be eligible if they are not currently in the United States and cannot prove that they have been
  physically present in the United States for a period of not less than 5 years immediately preceding today's date. Deferred action requests are decided on a case-by-case basis. DHS cannot provide any assurance that all such requests will be granted.
  The use of prosecutorial discretion confers no substantive right, immigration status, or pathway to citizenship. Only the Congress, acting through its legislative authority, can confer these rights.</p>
<p>While this guidance takes effect immediately, USCIS and ICE expect to begin implementation of the application processes within sixty days. In the meantime, individuals seeking more information on the new policy should visit USCIS's website (at www.uscis.gov),
  ICE's website (at www.ice.gov), or DHS's website (at www.dhs.gov). Beginning Monday, individuals can also call USCIS' hotline at 1-800-375-5283 or ICE's hotline at 1-888-351-4024 during business hours with questions or to request more information on
  the forthcoming process.</p>
<p>For individuals who are in removal proceedings and have already been identified as meeting the eligibility criteria and have been offered an exercise of discretion as part of ICE's ongoing case-by-case review, ICE will immediately begin to offer them
  deferred action for a period of two years, subject to renewal.</p>
