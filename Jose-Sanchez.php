    <h1>Jose Sanchez</h1>
    <p><strong>Jose Sanchez </strong>was born in Mexico City, Mexico and raised in <strong>Longview, Texas</strong>. Jose attended Spring Hill ISD from kindergarten till graduation in 1994 where he finished as the class Salutatorian. He then attended Kilgore College on an academic scholarship and finished his basic classes. Jose then attended Texas A&M University in College Station, Texas, on another academic scholarship and received his B.A. in 1998. After graduating from college, Jose attended Texas Tech Law School in Lubbock, Texas. He received his J.D. in 2001.</p>
    <p>After completing law school, Jose returned to Longview to help the people. He worked at Akin, Neely, & Mason, where he primarily worked on defense litigation matters. In 2002, Jose went and worked for the prestigious law firm of Wellborn & Houston in Henderson, Texas. At Wellborn & Houston, Jose worked primary on personal injury cases.</p>
    <p>In 2005, Jose decided to come back to his hometown and opened his own law firm in Longview, Texas. His practice consists mainly of personal injury actions, including product liability claims and wrongful death actions. He also handles a great amount of family immigration cases. Jose also handles a variety of litigation matters, contract disputes, insurance claims and employment issues as well as criminal defense and family law. Jose's practice mainly consists of cases in state courts throughout East Texas and the Dallas/Ft Worth area. During Jose's time, Jose tried over several jury trials to verdict, both in state and federal court.</p>
    <p>Outside of Jose's dedication to helping people with the law, he is very active in the community. Jose is bilingual and enjoys running, working out, sports, traveling, and computers.</p>
